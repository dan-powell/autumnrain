Headings
--------

Content can be separated into sections by using headings. There are six different levels of heading, numbered one to six. Each heading can be used multiple times within a document as long as the order of depth remains consistent. Headings have an automatic margin above and below. Each heading has a visual weight that should suggest it’s position within a document.

# Heading 1 [Link](#)

Used as the title for a page. Only used once per page or section. Should contain keywords relevant to the section it heads.

## Heading 2 [Link](#)

Used as a sub-title. Often used to divide an article in to sections.

### Heading 3 [Link](#)

A sub-title. Often used as a sub-heading. 

#### Heading 4 [Link](#)

A sub-title. Often used as a sub-heading.

##### Heading 5 [Link](#)

A sub-title. Often used as a sub-heading.

###### Heading 6 [Link](#)

A sub-title. Often used as a sub-heading.


