# Basic Typography {.pageHeading}


Body Text
---------

This is body text. It is the default style for all text in this document. Create new lines within a paragraph by holding `SHIFT` & `ENTER` like so: Simply pressing `ENTER` will create a new paragraph with a gap between the last like this:

**Here is some text with a few inline styles.** Some numbers – 1234567890. Quisque eget odio ac lectus *emphasis (italic)* faucibus **strong (bold)** in metus. In pellentesque faucibus <sup>superscript</sup> vestibulum. luctus tortor. [Inline link - should be very apparent that it is ‘clickable’](#) Nulla at nulla justo, eget.

Paragraphs should be about the same width as this sentence, (45 to 75 characters long).

Justified text should be 40 characters *minimum*.


